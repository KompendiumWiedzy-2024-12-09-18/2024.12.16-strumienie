package com.comarch.szkolenia.strumienie.generyki;

public class DoublePair {
    private double x;
    private double y;

    public DoublePair(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
